  <h5>제4조(개인정보의 파기절차 및 파기방법)</h5>
  <p>
    ① 한영넉스 는 원칙적으로 개인정보 수집∙이용 목적이 달성된 경우 또는 회원의
    탈퇴 요청이 온 경우에는 지체없이 해당 개인정보를 파기합니다.파기의 절차,
    기한 및 방법은 다음과 같습니다.
  </p>
  <p>
    ② 정보주체로부터 동의받은 개인정보 보유기간이 경과하거나 처리목적이
    달성되었음에도 불구하고 다른 법령에 따라 개인정보를 계속 보존하여야 하는
    경우에는, 해당 개인정보를 별도의 데이터베이스(DB)로 옮기거나 보관 장소를
    달리하여 보존합니다.
  </p>
  <p>③ 개인정보 파기의 절차 및 방법은 다음과 같습니다.</p>
  <ol>
    <li>
      <p>
        파기절차 한영넉스는 파기 사유가 발생한 개인정보를 선정하고, 개인정보
        보호책임자의 승인을 받아 개인정보를 파기합니다.
      </p>
    </li>
    <li>
      <p>
        파기방법 한영넉스는 전자적 파일형태로 기록/저장된 개인정보는 기록을
        재생할 수 없도록 로우레벨포멧(Low Level Format) 등의 방법을 이용하여
        파기하며, 종이 문서에 기록, 저장된 개인정보는 분쇄하거나 소각하여
        파기합니다.
      </p>
    </li>
  </ol>
  <h5>제5조(정보주체와 법정대리인의 권리·의무 및 그 행사방법에 관한 사항)</h5>
  <p>
    ① 정보주체는 한영넉스에 대해 언제든지 개인정보 열람·정정·삭제·처리정지 요구
    등의 권리를 행사할 수 있습니다.
  </p>
  <p>
