  <h1>개인정보 수집</h1>
  <p>
    한영넉스는 「개인정보 보호법」 제30조에 따라 정보주체의 개인정보를 보호하고
    이와 관련한 고충을 신속하고 원활하게 처리할 수 있도록 하기 위하여 다음과
    같이 개인정보 처리방침을 수립·공개합니다.
  </p>
  <p>○ 이 개인정보처리방침은 2022년 1월 1부터 적용됩니다.</p>
  <h5>제1조(개인정보의 처리 목적)</h5>
  <p>
    한영넉스는 다음의 목적을 위하여 개인정보를 처리합니다. 처리하고 있는
    개인정보는 다음의 목적 이외의 용도로는 이용되지 않으며 이용 목적이 변경되는
    경우에는 「개인정보 보호법」 제18조에 따라 별도의 동의를 받는 등 필요한
    조치를 이행할 예정입니다.
  </p>
  <ol>
    <li>
      <p>
        홈페이지 회원가입 및 관리 회원 가입의사 확인, 회원제 서비스 제공에 따른
        본인 식별·인증, 회원자격 유지·관리, 서비스 부정이용 방지, 각종
        고지·통지, 고충처리 목적으로 개인정보를 처리합니다.
      </p>
    </li>
    <li>
      <p>
        재화 또는 서비스 제공 서비스 제공, 콘텐츠 제공을 목적으로 개인정보를
        처리합니다.
      </p>
    </li>
    <li>
      <p>
        마케팅 및 광고에의 활용 신규 서비스(제품) 개발 및 맞춤 서비스 제공,
        접속빈도 파악 또는 회원의 서비스 이용에 대한 통계 등을 목적으로
        개인정보를 처리합니다.
      </p>
    </li>
  </ol>
